fun main() {
    // variables
    var base : Float = 10f
    var height : Float = 2f
    var area : Float = 0f
    var a : Float = 2f
    var b : Float = 1f
    var c : Float = 0f

    // process
    area = (base * height) / 2
    c = (a * a + 2 * a * b - 4 * (b + a)) / 2

    // output
    println("Area of the Triangle is: $area")
    println("The value of c is: $c")
}